// This header defines the ANSI color codes.
#define BLACK   "\033[0;30m"  // Normal black color
#define RED     "\033[0;31m"  // Normal red color
#define GREEN   "\033[0;32m"  // Normal green color
#define YELLOW  "\033[0;33m"  // Normal yellow color
#define BLUE    "\033[0;34m"  // Normal blue color
#define MAGENTA "\033[0;35m"  // Normal magenta color
#define CYAN    "\033[0;36m"  // Normal cyan color
#define WHITE   "\033[0;37m"  // Normal white color

#define BBLACK   "\033[1;30m" // Bold black color
#define BGRAY    "\033[1;90m" // Bold gray color
#define BRED     "\033[1;31m" // Bold red color
#define BGREEN   "\033[1;32m" // Bold green color
#define BYELLOW  "\033[1;33m" // Bold yellow color
#define BBLUE    "\033[1;34m" // Bold blue color
#define BMAGENTA "\033[1;35m" // Bold magenta color
#define BCYAN    "\033[1;36m" // Bold cyan color
#define BWHITE   "\033[1;37m" // Bold white color
#define BITAL    "\033[1;3m"  // Bold italic

#define DIM     "\033[2;m"    // Dim color
#define RESET   "\033[0;m"    // Reset color to default

